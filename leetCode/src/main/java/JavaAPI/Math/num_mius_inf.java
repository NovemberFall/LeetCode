package JavaAPI.Math;

class num_mius_inf {
    public static void main(String[] args) {
        int res = 5 - Integer.MAX_VALUE;
        System.out.println(res);
        System.out.println(Integer.MAX_VALUE);
        System.out.println("*****************************************");
        res = Integer.MAX_VALUE - 5;
        System.out.println(res);
        System.out.println(Integer.MAX_VALUE);
    }
}
